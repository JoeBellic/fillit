int		ft_tabt(t_list *alist)
{
	int i;
	int nb;

	nb = 0;
	while (alist)
		nb++;
	i = 0;
	if (nb > 1)
	{
		while (i * i < nb)
			i++;
		return (i * 2);
	}
	if (nb == 1)
		return (2);
	return (0);
}
